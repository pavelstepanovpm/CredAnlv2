    # –ü–æ–ø—Ä–æ–±—É–µ–º —Ä–∞–∑–Ω—ã–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–∏
    for user in root ubuntu admin; do
        log_info "–ü—Ä–æ–±—É–µ–º –ø–æ–¥–∫–ª—é—á–∏—Ç—å—Å—è –∫–∞–∫ $user..."
        if ssh -i ~/.ssh/DSkey_project -o ConnectTimeout=10 $user@$SERVER_HOST "echo 'SSH –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ —Ä–∞–±–æ—Ç–∞–µ—Ç!'" 2>/dev/null; then
