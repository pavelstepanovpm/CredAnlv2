        # –ê–∫—Ç–∏–≤–∏—Ä–æ–≤–∞—Ç—å –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—é
        ln -s /etc/nginx/sites-available/credit-analytics /etc/nginx/sites-enabled/
        
        # –ü—Ä–æ–≤–µ—Ä–∏—Ç—å –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—é
        nginx -t
        
        # –ü–µ—Ä–µ–∑–∞–≥—Ä—É–∑–∏—Ç—å Nginx
        systemctl reload nginx
        
        echo "‚úÖ Nginx –Ω–∞—Å—Ç—Ä–æ–µ–Ω"
